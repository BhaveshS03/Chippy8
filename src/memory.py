import os

class memory_alocate:
    def __init__(self,path):
        self.mem=[0]*4096
        self.pc=0x200
        self.rom = open(path,"rb")
        self.fontset=[0xF0, 0x90, 0x90, 0x90, 0xF0,  # 0
            0x20, 0x60, 0x20, 0x20, 0x70,  # 1
            0xF0, 0x10, 0xF0, 0x80, 0xF0,  # 2
            0xF0, 0x10, 0xF0, 0x10, 0xF0,  # 3
            0x90, 0x90, 0xF0, 0x10, 0x10,  # 4
            0xF0, 0x80, 0xF0, 0x10, 0xF0,  # 5
            0xF0, 0x80, 0xF0, 0x90, 0xF0,  # 6
            0xF0, 0x10, 0x20, 0x40, 0x40,  # 7
            0xF0, 0x90, 0xF0, 0x90, 0xF0,  # 8
            0xF0, 0x90, 0xF0, 0x10, 0xF0,  # 9
            0xF0, 0x90, 0xF0, 0x90, 0x90,  # A
            0xE0, 0x90, 0xE0, 0x90, 0xE0,  # B
            0xF0, 0x80, 0x80, 0x80, 0xF0,  # C
            0xE0, 0x90, 0x90, 0x90, 0xE0,  # D
            0xF0, 0x80, 0xF0, 0x80, 0xF0,  # E
            0xF0, 0x80, 0xF0, 0x80, 0x80]  # F
    def read_rom(self):
        self.romdata=self.rom.read()
        self.rom.seek(0,os.SEEK_END)
        size = self.rom.tell()
        print("done rom reading, size=",size)

        self.mem[:80]=bytearray(self.fontset)
        for i in range(size):
            self.mem[512+i]=self.romdata[i]

        return size
    def get_mem(self):
        #print("Memory acessed")
        return self.mem
